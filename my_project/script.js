const canvas = document.getElementById("circuit-canvas");
const context = canvas.getContext("2d");
let width = 0;
let height = 0;
let dpr = Math.max(1, window.devicePixelRatio || 1);
let nodes = [];
const mouse = {
  x: 0,
  y: 0,
  active: false,
};

function createNodes(count) {
  return Array.from({ length: count }, () => ({
    x: Math.random() * width,
    y: Math.random() * height,
    vx: (Math.random() - 0.5) * 0.35,
    vy: (Math.random() - 0.5) * 0.35,
    radius: Math.random() * 1.7 + 0.8,
  }));
}

function resizeCanvas() {
  dpr = Math.max(1, window.devicePixelRatio || 1);
  width = window.innerWidth;
  height = window.innerHeight;
  canvas.width = width * dpr;
  canvas.height = height * dpr;
  canvas.style.width = `${width}px`;
  canvas.style.height = `${height}px`;
  context.setTransform(dpr, 0, 0, dpr, 0, 0);

  const targetCount = Math.max(30, Math.min(64, Math.floor((width * height) / 24000)));
  nodes = createNodes(targetCount);
}

function drawConnection(a, b, distance) {
  const maxDistance = 182;
  if (distance > maxDistance) {
    return;
  }

  const alpha = 1 - distance / maxDistance;
  context.strokeStyle = `rgba(126, 242, 255, ${alpha * 0.28})`;
  context.lineWidth = 1;
  context.beginPath();
  context.moveTo(a.x, a.y);
  context.lineTo(b.x, b.y);
  context.stroke();
}

function animateCanvas() {
  context.clearRect(0, 0, width, height);

  for (const node of nodes) {
    node.x += node.vx;
    node.y += node.vy;

    if (node.x <= 0 || node.x >= width) {
      node.vx *= -1;
    }

    if (node.y <= 0 || node.y >= height) {
      node.vy *= -1;
    }
  }

  for (let i = 0; i < nodes.length; i += 1) {
    const a = nodes[i];

    for (let j = i + 1; j < nodes.length; j += 1) {
      const b = nodes[j];
      const distance = Math.hypot(a.x - b.x, a.y - b.y);
      drawConnection(a, b, distance);
    }

    if (mouse.active) {
      const mouseDistance = Math.hypot(a.x - mouse.x, a.y - mouse.y);
      if (mouseDistance < 220) {
        drawConnection(a, mouse, mouseDistance);
      }
    }

    context.beginPath();
    context.fillStyle = "rgba(126, 242, 255, 0.95)";
    context.shadowColor = "rgba(126, 242, 255, 0.7)";
    context.shadowBlur = 10;
    context.arc(a.x, a.y, a.radius, 0, Math.PI * 2);
    context.fill();
    context.shadowBlur = 0;
  }

  requestAnimationFrame(animateCanvas);
}

window.addEventListener("pointermove", (event) => {
  mouse.x = event.clientX;
  mouse.y = event.clientY;
  mouse.active = true;
});

window.addEventListener("pointerdown", () => {
  mouse.active = true;
});

window.addEventListener("pointerleave", () => {
  mouse.active = false;
});

window.addEventListener("resize", resizeCanvas);

resizeCanvas();
animateCanvas();
